package model;

public class Cell {
    private CellState state;

    public Cell() {
        this.state = CellState.EMPTY;
    }

    public String getRepresentation() {
        return state.getRep();
    }

    public void setState(CellState state) {
        this.state = state;
    }
    public CellState getState() {
        return state;
    }

    public boolean isEmpty() {
        return state == CellState.EMPTY;
    }
}
